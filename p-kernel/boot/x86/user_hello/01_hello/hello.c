/*
 *  01_hello/hello.c — p-kernel 最初のユーザープログラム (Hello World)
 *
 *  このプログラムは p-kernel 上で動く最もシンプルなユーザー空間プログラムです。
 *  「ring-3」と呼ばれるユーザー特権レベルで動き、
 *  「INT 0x80」命令でカーネルにシステムコールを発行します。
 *
 *  ─────────────────────────────────────────────
 *  p-kernel のメモリモデル
 *  ─────────────────────────────────────────────
 *
 *    0x00000000  ┌─────────────────────┐
 *                │   カーネル空間       │  ring-0（特権モード）
 *    0x00400000  ├─────────────────────┤  ← このプログラムのロードアドレス
 *                │   ユーザー空間       │  ring-3（非特権モード）
 *                └─────────────────────┘
 *
 *  ユーザープログラムから直接ハードウェアやカーネルデータには触れません。
 *  カーネルの機能を使うには「システムコール（INT 0x80）」を使います。
 *
 *  ─────────────────────────────────────────────
 *  plibc.h について
 *  ─────────────────────────────────────────────
 *  plibc.h は p-kernel 専用の軽量ユーザーライブラリです。
 *  glibc などの標準 C ライブラリは使わず、INT 0x80 を直接呼び出します。
 *
 *  ビルド方法:
 *    make 01_hello/hello.elf
 *
 *  QEMU 実行方法:
 *    p-kernel> exec hello.elf
 */

#include "plibc.h"   /* p-kernel ユーザー空間ライブラリ */

/*
 *  _start — プログラムのエントリーポイント
 *
 *  通常の C プログラムなら main() から始まりますが、
 *  libc を使わないベアメタル ELF では _start() が直接呼ばれます。
 *  リンカスクリプト (user.ld) で ENTRY(_start) と指定されています。
 */
void _start(void)
{
    /* ── シリアルポートへの出力 ──────────────────────────────────
     *
     * plib_puts() は内部で sys_write(fd=1, ...) を呼びます。
     * fd=1 は「標準出力」で、p-kernel ではシリアルポート (UART) に
     * マッピングされています。QEMU の端末に出力が表示されます。
     */
    plib_puts("Hello, p-kernel!\r\n");
    plib_puts("\r\n");

    /* ── システムコールの仕組みを説明する出力 ─────────────────── */
    plib_puts("このプログラムは ring-3 (ユーザー空間) で動いています。\r\n");
    plib_puts("INT 0x80 でカーネルにシステムコールを発行しています。\r\n");
    plib_puts("\r\n");

    /* ── 数値出力のデモ ─────────────────────────────────────────
     *
     * plib_puti() で符号付き整数を出力できます。
     */
    plib_puts("簡単な計算: 6 x 7 = ");
    plib_puti(6 * 7);     /* 結果: 42 */
    plib_puts("\r\n");

    plib_puts("カウントダウン: ");
    for (int i = 3; i >= 1; i--) {
        plib_puti(i);
        plib_puts("... ");
    }
    plib_puts("完了!\r\n");
    plib_puts("\r\n");

    /* ── 次のステップ案内 ───────────────────────────────────────
     *
     * 次は 02_posix_io で FAT32 ファイルシステムへの読み書きを試してみましょう。
     *   p-kernel> exec posix_io.elf
     */
    plib_puts("次のサンプル: exec posix_io.elf\r\n");

    /* ── プロセス終了 ───────────────────────────────────────────
     *
     * sys_exit() は SYS_EXIT (番号 1) システムコールを呼びます。
     * カーネルはこのタスクを終了させ、シェルに戻ります。
     * 引数 0 は「正常終了」を意味します。
     */
    sys_exit(0);
}
